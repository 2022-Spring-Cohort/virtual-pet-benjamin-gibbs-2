package virtual_pet;

public class OrganicTressym {
    private String tressymName;
    private int tressymAge;

    public OrganicTressym(String tressymName, int tressymAge) {
        this.tressymName = tressymName;
        this.tressymAge = tressymAge;
    }

    public String getTressymName() {
        return tressymName;
    }

    public int getTressymAge() {
        return tressymAge;
    }
}
