package virtual_pet;

public class OrganicLlama {
    private String llamaName;
    private int llamaAge;


    public OrganicLlama(String llamaName, int llamaAge) {
        this.llamaName = llamaName;
        this.llamaAge = llamaAge;
    }

    public String getLlamaName() {
        return llamaName;
    }

    public int getLlamaAge() {
        return llamaAge;
    }
}




